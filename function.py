def tambah(angka1, angka2):
    hasil = angka1 + angka2
    print(f"pertambahan dari angka {angka1} + {angka2} = {hasil}")

def kurang(angka1, angka2):
    hasil = angka1 - angka2
    print(f"pengurangan dari angka {angka1} - {angka2} = {hasil}")

def kali(angka1, angka2):
    hasil = angka1 * angka2
    print(f"perklaian dari angka {angka1} - {angka2} = {hasil}")

def bagi(angka1, angka2):
    hasil = angka1 / angka2
    print(f"pembagian dari angka {angka1} - {angka2} = {hasil}")